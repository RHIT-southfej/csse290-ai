package AIGeneratedCode;

public class ChessPiece {
    private final Color color;
    private final PieceType type;

    public ChessPiece(Color color, PieceType type) {
        this.color = color;
        this.type = type;
    }

    public Color getColor() {
        return color;
    }

    public PieceType getType() {
        return type;
    }
}
